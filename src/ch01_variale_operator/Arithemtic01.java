package ch01_variale_operator;

public class Arithemtic01 {
    static void main() {
        //연산자 ~~ ~우왕 2일차 하고도 2시간 만에 드디어 연산자에들어간다~~`
        int a = 14 , b = 5;
        System.out.println("================");
        System.out.println(" a + b = "+ (a+b));
        System.out.println("================");
        System.out.println(" a - b = "+ (a-b));
        System.out.println("================");
        System.out.println(" a / b = "+ (a/b));
        System.out.println("================");
        System.out.println(" a * b = "+(a*b));
        System.out.println("================");
        System.out.println(" a % b = "+ (a%b));
        System.out.println("================");
    }
}
